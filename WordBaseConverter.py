    def _int2wordBase(self, numberIn, toSet):
        # make an abs integer out of the numberIn
        x = abs(numberIn) 

        # create the result in base 'len(toSet)'
        outList = list()
        delim=self.WORD_DELIM
        if x == 0:
            outList.append(toSet[0])
        else:
            while x > 0:
                digit = x % len(toSet)
                outList.insert(0,toSet[digit])
                x = int(x // len(toSet))
        return delim.join(outList)

    def _wordBase2int(self, wordsIn, from_digits, toSet):
        # split on dashes
        x = 0
        #print "DBUG: wordsIn[{0}]".format(wordsIn)
        for digit in wordsIn.split("-"):
            #print "DBUG: digit[{0}]".format(digit)
            try:
                x = x * len(from_digits) + from_digits.index(digit.lower())
            except ValueError:
                raise ValueError('invalid word "%s"' % digit)

        # create the result in base 'len(toSet)'
        if x == 0:
            res = toSet[0]
        else:
            res = ''
            while x > 0:
                digit = x % len(toSet)
                res = toSet[digit] + res
                x = int(x // len(toSet))
        return res

